float sum(float a,float b) {
    float s;
    s=a+b;
    return (s);
}
float diff(float a,float b) {
    float d;
    d=a-b;
    return (d);
}
float prod(float a,float b) {
    float p;
    p=a*b;
    return (p);
}
float quot(float a,float b) {
    float q;
    q=a/b;
    return (q);
}
int remainder(int a,int b) {
    int r;
    r=a%b;
    return r;
}